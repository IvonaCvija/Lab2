// boolean
var fact = true;
console.log("Value assigned to fact is " + fact);
// number
var decimal = 25;
console.log("Value assigned to decimal is " + decimal);
// color
var color = "pink";
console.log("Value assigned to color is " + color);
// array
var array = [2, 4, 8, 16, 32, 64, 122];
for (var i = 0; i < 7; i++) {
    console.log("Values assigned to array are " + array[i]);
}
// string
var word = 'puppy';
console.log("Value assigned to string " + word);
